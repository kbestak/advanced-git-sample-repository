import numpy as np


def add(x: np.int32, y: np.int32):
    return x + y

def subtract(x: np.int32, y: np.int32):
    return x - y

def multiply(x: np.int32, y: np.int32):
    return x * y

def sub_1(x: np.int32, y: np.int32):
    return x - y - 1

def sub_2(x: np.int32, y: np.int32):
    return x - y - 2